// 奖励池：按稀有度过滤（供战斗奖励 / 商店抽卡用）
export function cardsByRarity(rarity: Card['rarity']): Card[] {
  return CARDS.filter((c) => c.rarity === rarity && c.type !== 'curse');
}

// 可作为奖励的卡（排除诅咒、事件牌）
export const REWARD_POOL: Card[] = CARDS.filter(
  (c) => c.type !== 'curse' && c.type !== 'event_card',
);
