/** 奖励池：非基础、非诅咒 */
export const REWARD_POOL: CardTemplate[] = CARD_TEMPLATES.filter(
  (c) => c.type !== 'curse' && c.rarity !== 'basic',
);

export function getTemplate(id: string): CardTemplate | undefined {
  return TEMPLATE_MAP[resolveTemplateId(id)];
}

export function getCard(id: string): Card {
  const template = getTemplate(id);
  if (!template) throw new Error(`Unknown card template: ${id}`);
  return createCardInstance(template);
}

export function cloneCard(card: Card): Card {
  const template = getTemplate(card.id);
  if (!template) return { ...card };
  return reResolveInstance(template, {
    templateId: card.id,
    instanceId: card.instanceId,
    nurtureLevel: card.nurtureLevel,
    keywords: { ...card.keywords },
  });
}

export function buildDeckFromIds(ids: string[]): Card[] {
  return ids.map((id) => getCard(id));
}
